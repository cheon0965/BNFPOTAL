export function buildCustomerHelpSections(raw) {
    const lines = raw.split('\n').map((line) => line.trim());
    const sections = [];
    let current = null;
    let note = '';

    lines.forEach((line) => {
        if (!line) {
            return;
        }

        if (line === 'ERP 유지보수 포털 화면별 도움말' || line === '고객 화면') {
            return;
        }

        if (line.startsWith('참고:')) {
            note = line;
            return;
        }

        const isSectionTitle =
            !line.startsWith('구분:') &&
            !line.startsWith('사용 목적:') &&
            !line.startsWith('사용 방법:') &&
            !/^[0-9]+[.)]/.test(line);

        if (isSectionTitle) {
            if (current) {
                sections.push(current);
            }
            current = {
                title: line,
                scope: '',
                purpose: '',
                stepLines: [],
                bodyLines: [],
            };
            return;
        }

        if (!current) {
            return;
        }

        if (line.startsWith('구분:')) {
            current.scope = line.replace(/^구분:\s*/, '').trim();
        } else if (line.startsWith('사용 목적:')) {
            current.purpose = line.replace(/^사용 목적:\s*/, '').trim();
        } else if (line.startsWith('사용 방법:')) {
            // label only
        } else if (/^[0-9]+[.)]/.test(line)) {
            current.stepLines.push(line);
        } else {
            current.bodyLines.push(line);
        }
    });

    if (current) {
        sections.push(current);
    }

    return { sections, note };
}
